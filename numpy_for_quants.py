
import numpy as np

# Creating arrays
arr1 = np.array([1, 2, 3, 4, 5])
print("Array:", arr1)

# Mathematical operations
arr2 = np.array([5, 4, 3, 2, 1])
sum_arr = arr1 + arr2
print("Sum of arrays:", sum_arr)

# Statistical functions
mean_val = np.mean(arr1)
std_val = np.std(arr1)
print("Mean:", mean_val, "Standard Deviation:", std_val)
